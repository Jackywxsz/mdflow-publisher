import { RedNoteTemplatePreset } from './types';

function createTemplate(
  id: string,
  name: string,
  description: string,
  showCover: boolean,
  variables: Record<string, string>
): RedNoteTemplatePreset {
  return { id, name, description, showCover, variables };
}

function createMinimalTemplate(
  id: string,
  name: string,
  description: string,
  overrides: Record<string, string>
): RedNoteTemplatePreset {
  return createTemplate(id, name, description, false, {
    ...baseMinimalVariables,
    ...overrides,
  });
}

